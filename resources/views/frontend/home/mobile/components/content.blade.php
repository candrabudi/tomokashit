    <script>
        document.addEventListener('DOMContentLoaded', function() {
            const prevButton = document.getElementById('prevButton');
            const nextButton = document.getElementById('nextButton');
            const imageContainer = document.getElementById('imageContainer');

            // Fungsi untuk memperbarui status tombol
            function updateButtonState() {
                const maxScrollLeft = imageContainer.scrollWidth - imageContainer.clientWidth;
                prevButton.disabled = imageContainer.scrollLeft === 0;
                nextButton.disabled = imageContainer.scrollLeft >= maxScrollLeft;
            }

            // Tombol kiri
            prevButton.addEventListener('click', function() {
                imageContainer.scrollBy({
                    left: -200,
                    behavior: 'smooth'
                });
                // Memberikan sedikit delay untuk memastikan perubahan scroll
                setTimeout(updateButtonState, 200);
            });

            // Tombol kanan
            nextButton.addEventListener('click', function() {
                imageContainer.scrollBy({
                    left: 200,
                    behavior: 'smooth'
                });
                // Memberikan sedikit delay untuk memastikan perubahan scroll
                setTimeout(updateButtonState, 200);
            });

            // Cek status tombol awal
            updateButtonState();

            // Memperbarui status tombol saat scroll berubah
            imageContainer.addEventListener('scroll', function() {
                updateButtonState();
            });
        });
    </script>



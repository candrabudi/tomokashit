<script>
    function showContent(tab) {
        // Hide all content
        document.querySelectorAll('.tab-content').forEach(function(content) {
            content.style.display = 'none';
        });

        // Remove active class from all tabs
        document.querySelectorAll('.tab').forEach(function(tabElement) {
            tabElement.classList.remove('bg-primary');
            tabElement.classList.add('bg-paper');
            tabElement.classList.remove('color-white');
        });

        // Show selected tab's content and highlight the tab
        document.getElementById('content-' + tab).style.display = 'block';
        document.getElementById('tab-' + tab).classList.add('bg-primary');
        document.getElementById('tab-' + tab).classList.add('color-white');
    }
</script>
